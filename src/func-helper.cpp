// --function to print usage--
void print_usage() {
        std::cout << R"(Usage:
    gitbully --folder <folder-to-scan> --patterns <patterns-file-txt> --output <output-file>
        or
    gitbully --repo <repo_url> --patterns <patterns-file-txt>

    Options:
    --folder     Path to the local folder to scan
    --repo       URL of the remote repository to scan
    --patterns   Path to the text file containing patterns to search
    --output     Path to the file where results will be saved
    --help       Show the usage of the tool
    )" << std::endl;
}

std::string run(std::string command){

	std::array<char , 128> buffer;
	std::string output;

	FILE* pipe = popen(command.c_str(),"r");

	if(!pipe) return "Failed to run command :: " + command;

	while(fgets(buffer.data() , buffer.size() , pipe)){
		output += buffer.data();
	}

	pclose(pipe);

    if (!output.empty() && output.back() == '\n') {
        output.pop_back();
    }

    return output;
}